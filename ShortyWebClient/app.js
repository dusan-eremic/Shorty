$(document).ready(function() {

	$( "#submitUrl" ).click(function() {  
	  $("#shortUrlDisplay").hide();
	  var enteredUrl = $( "#originalUrl" ).val();
	  if (!enteredUrl) {
	  	$( "#validationMessage" ).text( "Please enter your URL first!" ).show().fadeOut( 2000 );
	  }
	  else if(!isUrlValid(enteredUrl)) {
	  	$( "#validationMessage" ).text( "Sorry, entered URL is not valid!" ).show().fadeOut( 2000 );	
	  } 
	  else {
	  	sumbitUrl($( "#originalUrl" ).val());
	  }
	});
});

function sumbitUrl(originalUrl) {
	$.ajax({
    type: 'POST',
    url: 'http://localhost:8080/urls',
    data: JSON.stringify ({url: originalUrl}),
    success: function(data) { displayShortUrl(data.shortUrl)},
    contentType: "application/json",
    dataType: 'json'
	});
}

function displayShortUrl(shortUrl) {
	$("#shortUrl").prop("href", shortUrl).text(shortUrl);
	$("#shortUrlDisplay").show();
}

